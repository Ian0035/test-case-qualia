from __future__ import annotations

import json
from dataclasses import asdict
from datetime import datetime, timezone
from pathlib import Path

from qualia_lerobot_augmentor.config import AugmentationPreset
from qualia_lerobot_augmentor.video import VideoProcessingSummary


def write_manifest(
    dataset_dir: Path,
    source: str,
    output_repo_id: str | None,
    preset: AugmentationPreset,
    seed: int,
    summaries: list[VideoProcessingSummary],
) -> Path:
    manifest = {
        "created_at_utc": datetime.now(timezone.utc).isoformat(),
        "source_dataset": source,
        "output_repo_id": output_repo_id,
        "augmentation_type": "photometric_video_domain_randomization",
        "preset": preset.to_dict(),
        "seed": seed,
        "video_files_processed": len(summaries),
        "total_frames_processed": sum(item.frame_count for item in summaries),
        "videos": [asdict(summary) for summary in summaries],
    }
    manifest_path = dataset_dir / "meta" / "augmentation_manifest.json"
    manifest_path.write_text(json.dumps(manifest, indent=2), encoding="utf-8")
    return manifest_path


def write_dataset_card(
    dataset_dir: Path,
    source: str,
    output_repo_id: str | None,
    preset: AugmentationPreset,
    summaries: list[VideoProcessingSummary],
) -> Path:
    total_frames = sum(item.frame_count for item in summaries)
    first_transform = summaries[0].transform if summaries else {}
    repo_line = output_repo_id or "local-only run"
    readme_path = dataset_dir / "README.md"
    contents = f"""# Qualia LeRobot Augmented Dataset

This dataset was generated by the `qualia-lerobot-augmentor` tool.

## Provenance

- Source dataset: `{source}`
- Output repo: `{repo_line}`
- Augmentation strategy: `photometric_video_domain_randomization`
- Preset: `{preset.name}`
- Processed video files: `{len(summaries)}`
- Processed frames: `{total_frames}`

## What changed

The LeRobot v3 format stores camera streams in `videos/` as MP4 shards, so this tool preserves the original Parquet and metadata layout and rewrites only the video files.

Each video shard receives a deterministic photometric transform sampled from the selected preset:

```json
{json.dumps(first_transform, indent=2)}
```

That makes the dataset more robust to changes in lighting, color response, and sensor noise while keeping episode timing and shard boundaries stable.
"""
    readme_path.write_text(contents, encoding="utf-8")
    return readme_path
